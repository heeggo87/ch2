# 문자 데이터를 위한 연산자
# ctrl + /

# +기호
print( 2 + 3 )
print( "2" + "3")
# 파이썬 파일 실행 : f6 또는 실행버튼

# 이식에서 +기호는 더하기 연산자
# 두항이 숫자면 +기호는 더하기연산자가 됨
print( 2 + 3 ) # 결과는 숫자 5

# 이식에서 +기호는 문자열 연결 연산자
# 두항이 문자면 +는 문자열 연결 연산자가됨

# 문자열 연결 연산자
print("2" + "3") # 결과는 문자열 "23"
print("안녕" + "하세요") # 안녕하세요
print("ab" + "cd" + "ef") #abcdef

# 문자열 반복 연산자
# 문자 * 횟수
print("a" * 3) #aaa
print("abc" * 5) #abcabcabcabcabc

# 동물 소리 내기
print("멍"*3 + "!") #멍멍멍
